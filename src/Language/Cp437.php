namespace Dogma\Language;

use Dogma\AsciiChars;
use Dogma\StaticClassMixin;
use function range;
use function str_replace;

class Cp437
{
    use StaticClassMixin;

    // lower half is ascii
    private const UPPER_HALF = [
        'Ç', 'ü', 'é', 'â', 'ä', 'à', 'å', 'ç', 'ê', 'ë', 'è', 'ï', 'î', 'ì', 'Ä', 'Å',
        'É', 'æ', 'Æ', 'ô', 'ö', 'ò', 'û', 'ù', 'ÿ', 'Ö', 'Ü', '¢', '£', '¥', '₧', 'ƒ',
        'á', 'í', 'ó', 'ú', 'ñ', 'Ñ', 'ª', 'º', '¿', '⌐', '¬', '½', '¼', '¡', '«', '»',
        '░', '▒', '▓', '│', '┤', '╡', '╢', '╖', '╕', '╣', '║', '╗', '╝', '╜', '╛', '┐',
        '└', '┴', '┬', '├', '─', '┼', '╞', '╟', '╚', '╔', '╩', '╦', '╠', '═', '╬', '╧',
        '╨', '╤', '╥', '╙', '╘', '╒', '╓', '╫', '╪', '┘', '┌', '█', '▄', '▌', '▐', '▀',
        'α', 'ß', 'Γ', 'π', 'Σ', 'σ', 'µ', 'τ', 'Φ', 'Θ', 'Ω', 'δ', '∞', 'φ', 'ε', '∩',
        '≡', '±', '≥', '≤', '⌠', '⌡', '÷', '≈', '°', '∙', '·', '√', 'ⁿ', '²', '■', ' ',
    ];

    private const SPECIAL_PRINTABLE = [
        '¤', '☺', '☻', '♥', '♦', '♣', '♠', '•', '◘', '○', '◙', '♂', '♀', '♪', '♫', '☼',
        '►', '◄', '↕', '‼', '¶', '§', '▬', '↨', '↑', '↓', '→', '←', '∟', '↔', '▲', '▼',
        '⌂',
    ];

    public static function toUtf8(string $string): string
    {
        return str_replace(range("\x80", "\xff"), self::UPPER_HALF, $string);
    }

    /**
     * Binary string formatted as UTF-8 representation of original IPM PC (CP437) font
     *
     * @param string $string
     * @return string
     */
    public static function toUtf8Printable(string $string): string
    {
        $string = self::toUtf8($string);

        return str_replace(AsciiChars::SPECIAL_CHARS, self::SPECIAL_PRINTABLE, $string);
    }

}
